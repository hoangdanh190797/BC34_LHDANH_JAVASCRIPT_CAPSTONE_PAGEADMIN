function Product(
  _id,
  _name,
  _price,
  _img,
  _desc,
  _screen,
  _backCamera,
  _frontCamera,
  _type
) {
  this.id = _id;
  this.name = _name;
  this.price = _price;
  this.img = _img;
  this.desc = _desc;
  this.screen = _screen;
  this.backCamera = _backCamera;
  this.frontCamera = _frontCamera;
  this.type = _type;
}
